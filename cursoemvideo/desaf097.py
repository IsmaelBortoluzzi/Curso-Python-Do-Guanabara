# Print especial com função:

def escreva(s):
    s = str(s)
    s.split()
    print('-'*len(s))
    print(s)
    print('-'*len(s))
    print('')


escreva('DEUTSCHLAND')
escreva('IM WALDE GRÜNE WALDE')
escreva('DER GOTT DER EISEN WACHSEN LIESS')
escreva('WESTWALD MARSCH')
escreva(12)

